var mapState = {
    map: null,
    markers: [],
    stations: [],
};

document.addEventListener("DOMContentLoaded", function () {
    initMap();
    initSlideshow();
    initRevealAnimations();
    initI18n();
});

function initSlideshow() {
    var slides = document.querySelectorAll(".slideshow-frame .slide");
    if (!slides.length) {
        return;
    }

    var currentIndex = 0;
    var frame = document.querySelector(".slideshow-frame");
    var prevButton = document.querySelector(".slide-nav.prev");
    var nextButton = document.querySelector(".slide-nav.next");
    var autoplayDelay = 5200;
    var autoplayTimer = null;

    function showSlide(index) {
        if (index === currentIndex) {
            return;
        }

        slides[currentIndex].classList.remove("is-active");
        slides[index].classList.add("is-active");
        currentIndex = index;
    }

    function nextSlide() {
        var nextIndex = (currentIndex + 1) % slides.length;
        showSlide(nextIndex);
    }

    function prevSlide() {
        var prevIndex = (currentIndex - 1 + slides.length) % slides.length;
        showSlide(prevIndex);
    }

    function resetAutoplay() {
        if (autoplayTimer) {
            clearInterval(autoplayTimer);
        }
        autoplayTimer = setInterval(nextSlide, autoplayDelay);
    }

    function stopAutoplay() {
        if (autoplayTimer) {
            clearInterval(autoplayTimer);
            autoplayTimer = null;
        }
    }

    if (nextButton) {
        nextButton.addEventListener("click", function () {
            nextSlide();
            resetAutoplay();
        });
    }

    if (prevButton) {
        prevButton.addEventListener("click", function () {
            prevSlide();
            resetAutoplay();
        });
    }

    if (frame) {
        frame.addEventListener("mouseenter", stopAutoplay);
        frame.addEventListener("mouseleave", resetAutoplay);
        frame.addEventListener("focusin", stopAutoplay);
        frame.addEventListener("focusout", resetAutoplay);
        frame.addEventListener("touchstart", stopAutoplay, { passive: true });
        frame.addEventListener("touchend", resetAutoplay, { passive: true });
    }

    document.addEventListener("visibilitychange", function () {
        if (document.hidden) {
            stopAutoplay();
        } else {
            resetAutoplay();
        }
    });

    resetAutoplay();
}

function initMap() {
    var mapElement = document.getElementById("map");
    if (!mapElement || typeof L === "undefined") {
        return;
    }

    mapState.map = L.map("map").setView([38.18, 22.2], 11);

    L.tileLayer("https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png", {
        attribution: "© OpenStreetMap",
    }).addTo(mapState.map);

    mapState.stations = [
        {
            nameKey: "station_diakopto_name",
            textKey: "station_diakopto_text",
            coords: [38.192, 22.201],
        },
        {
            nameKey: "station_niamata_name",
            textKey: "station_niamata_text",
            coords: [38.191, 22.262],
        },
        {
            nameKey: "station_zachlorou_name",
            textKey: "station_zachlorou_text",
            coords: [38.083, 22.207],
        },
        {
            nameKey: "station_kalavryta_name",
            textKey: "station_kalavryta_text",
            coords: [37.972, 22.111],
        },
    ];

    var trainIcon = L.icon({
        iconUrl: "assets/icons/train-icon.png",
        iconSize: [32, 32],
    });

    mapState.stations.forEach(function (station) {
        var marker = L.marker(station.coords, { icon: trainIcon }).addTo(mapState.map);
        mapState.markers.push({ marker: marker, station: station });
    });

    var route = [
        [38.192, 22.201],
        [38.191, 22.262],
        [38.083, 22.207],
        [37.972, 22.111],
    ];

    var line = L.polyline(route, {
        color: "#9e3b2c",
        weight: 4,
    }).addTo(mapState.map);

    mapState.map.fitBounds(line.getBounds(), { padding: [20, 20] });
}

function initRevealAnimations() {
    var revealElements = document.querySelectorAll("section, .card, .timeline-item, footer");

    revealElements.forEach(function (element) {
        element.classList.add("reveal");
    });

    var observer = new IntersectionObserver(
        function (entries) {
            entries.forEach(function (entry) {
                if (entry.isIntersecting) {
                    entry.target.classList.add("active");
                    observer.unobserve(entry.target);
                }
            });
        },
        {
            threshold: 0.16,
            rootMargin: "0px 0px -40px 0px",
        }
    );

    revealElements.forEach(function (element) {
        observer.observe(element);
    });
}

function initI18n() {
    var buttons = document.querySelectorAll(".lang-btn");

    buttons.forEach(function (button) {
        button.addEventListener("click", function () {
            setLanguage(button.getAttribute("data-lang"));
        });
    });

    setLanguage("el");
}

function setLanguage(lang) {
    if (!i18n[lang]) {
        return;
    }

    currentLanguage = lang;
    document.documentElement.lang = lang;
    document.title = i18n[lang].site_title;

    var textNodes = document.querySelectorAll("[data-i18n]");
    textNodes.forEach(function (node) {
        var key = node.getAttribute("data-i18n");
        if (i18n[lang][key]) {
            node.textContent = i18n[lang][key];
        }
    });

    var buttons = document.querySelectorAll(".lang-btn");
    buttons.forEach(function (button) {
        if (button.getAttribute("data-lang") === lang) {
            button.classList.add("is-active");
        } else {
            button.classList.remove("is-active");
        }
    });

    updateMapPopups();
}

function updateMapPopups() {
    if (!mapState.markers.length) {
        return;
    }

    mapState.markers.forEach(function (entry) {
        var stationName = i18n[currentLanguage][entry.station.nameKey];
        var stationText = i18n[currentLanguage][entry.station.textKey];
        entry.marker.bindPopup("<b>" + stationName + "</b><br>" + stationText);
    });
}
