function initNavActiveState() {
    var navLinks = document.querySelectorAll('.site-nav-link[href^="#"]');
    if (!navLinks.length) {
        return;
    }

    var homeLink = null;
    var navSectionPairs = [];
    var ticking = false;

    navLinks.forEach(function (link) {
        var targetId = link.getAttribute("href");
        if (!targetId) {
            return;
        }

        if (targetId === "#page-top") {
            homeLink = link;
            link.addEventListener("click", function () {
                emitAnalyticsEvent("nav_section_click", {
                    language: currentLanguage,
                    target: "page-top",
                });
                setActiveNavLink(link);
            });
            return;
        }

        var section = document.querySelector(targetId);
        if (!section) {
            return;
        }

        navSectionPairs.push({
            link: link,
            section: section,
        });

        link.addEventListener("click", function () {
            emitAnalyticsEvent("nav_section_click", {
                language: currentLanguage,
                target: targetId.replace("#", ""),
            });
            setActiveNavLink(link);
        });
    });

    function setActiveNavLink(activeLink) {
        navLinks.forEach(function (link) {
            if (link === activeLink) {
                link.classList.add("is-active");
            } else {
                link.classList.remove("is-active");
            }
        });
    }

    if (!navSectionPairs.length) {
        if (homeLink) {
            setActiveNavLink(homeLink);
        }
        return;
    }

    function updateActiveLinkByScroll() {
        ticking = false;

        var rootStyle = window.getComputedStyle(document.documentElement);
        var headerOffset = parseFloat(rootStyle.getPropertyValue("--header-offset")) || 72;
        var probeY = window.scrollY + headerOffset + 24;

        if (window.scrollY < 24 && homeLink) {
            setActiveNavLink(homeLink);
            return;
        }

        var activePair = navSectionPairs[0];
        navSectionPairs.forEach(function (pair) {
            var sectionTop = pair.section.offsetTop;
            if (sectionTop <= probeY) {
                activePair = pair;
            }
        });

        if (activePair && activePair.link) {
            setActiveNavLink(activePair.link);
        }
    }

    function requestActiveUpdate() {
        if (ticking) {
            return;
        }

        ticking = true;
        window.requestAnimationFrame(updateActiveLinkByScroll);
    }

    window.addEventListener("scroll", requestActiveUpdate, { passive: true });
    window.addEventListener("resize", requestActiveUpdate);

    requestActiveUpdate();
}

