function initHeroVideo() {
    var heroVideo = document.getElementById("hero-video");
    var heroSection = document.querySelector(".hero-split");

    if (!heroVideo) {
        return;
    }

    var preferredSrc = heroVideo.getAttribute("data-video-src") || heroVideo.getAttribute("data-src-mobile");
    var source = heroVideo.querySelector("source");
    var sourceDataSrc = source ? source.getAttribute("data-src") : "";
    var canEnhance = shouldEnhanceImmersiveMedia();

    function unloadVideo() {
        heroVideo.loop = false;
        heroVideo.preload = "none";
        heroVideo.pause();
        if (source) {
            source.setAttribute("src", "");
        }
        heroVideo.load();
    }

    function loadAndPlay() {
        var nextSrc = preferredSrc || sourceDataSrc;

        if (source && nextSrc && source.getAttribute("src") !== nextSrc) {
            source.setAttribute("src", nextSrc);
        }

        heroVideo.loop = true;
        heroVideo.preload = "metadata";
        heroVideo.load();

        var playAttempt = heroVideo.play();
        if (playAttempt && typeof playAttempt.catch === "function") {
            playAttempt.catch(function () {
                // Ignore autoplay promise rejections when browser policies intervene.
            });
        }
    }

    if (!canEnhance) {
        unloadVideo();
        return;
    }

    if (!heroSection || typeof window.IntersectionObserver !== "function") {
        runAfterWindowLoad(loadAndPlay, 1000);
        return;
    }

    var observer = new IntersectionObserver(
        function (entries) {
            entries.forEach(function (entry) {
                if (!entry.isIntersecting || entry.intersectionRatio < 0.35) {
                    return;
                }

                observer.disconnect();
                runAfterWindowLoad(loadAndPlay, 1000);
            });
        },
        {
            threshold: [0.35],
        }
    );

    observer.observe(heroSection);
}

