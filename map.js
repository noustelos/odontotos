var mapState = {
    map: null,
    markers: [],
    initialBounds: null,
};

document.addEventListener("DOMContentLoaded", function () {
    initMap();
    initRevealAnimations();
    initI18n();
});

function initMap() {
    var mapElement = document.getElementById("map");
    if (!mapElement || typeof L === "undefined") {
        return;
    }

    mapState.map = L.map("map").setView([38.18, 22.2], 11);

    L.tileLayer("https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png", {
        attribution: "© OpenStreetMap",
    }).addTo(mapState.map);

    var fallbackRoute = [
        [38.192, 22.201],
        [38.191, 22.262],
        [38.083, 22.207],
        [37.972, 22.111],
    ];

    function setInitialMapBounds(bounds) {
        if (bounds && bounds.isValid && bounds.isValid()) {
            mapState.initialBounds = bounds;
            mapState.map.fitBounds(bounds, { padding: [20, 20] });
        }
    }

    function drawFallbackRoute() {
        var line = L.polyline(fallbackRoute, {
            color: "#9e3b2c",
            weight: 4,
        }).addTo(mapState.map);
        setInitialMapBounds(line.getBounds());
    }

    fetch("assets/map/odontotos-route.geojson")
        .then(function (response) {
            if (!response.ok) {
                throw new Error("Failed to load route GeoJSON");
            }
            return response.json();
        })
        .then(function (geojsonData) {
            var routeLayer = L.geoJSON(geojsonData, {
                style: {
                    color: "#9e3b2c",
                    weight: 4,
                },
            }).addTo(mapState.map);

            setInitialMapBounds(routeLayer.getBounds());
        })
        .catch(function () {
            drawFallbackRoute();
        });

    var resetMapButton = document.getElementById("reset-map-view");
    if (resetMapButton) {
        resetMapButton.addEventListener("click", function () {
            if (mapState.initialBounds) {
                mapState.map.fitBounds(mapState.initialBounds, { padding: [20, 20] });
            } else {
                mapState.map.setView([38.18, 22.2], 11);
            }
        });
    }
}

function initRevealAnimations() {
    var revealElements = document.querySelectorAll("section, .card, .timeline-item, footer");

    revealElements.forEach(function (element) {
        element.classList.add("reveal");
    });

    var observer = new IntersectionObserver(
        function (entries) {
            entries.forEach(function (entry) {
                if (entry.isIntersecting) {
                    entry.target.classList.add("active");
                    observer.unobserve(entry.target);
                }
            });
        },
        {
            threshold: 0.16,
            rootMargin: "0px 0px -40px 0px",
        }
    );

    revealElements.forEach(function (element) {
        observer.observe(element);
    });
}

function initI18n() {
    var buttons = document.querySelectorAll(".lang-btn");

    buttons.forEach(function (button) {
        button.addEventListener("click", function () {
            setLanguage(button.getAttribute("data-lang"));
        });
    });

    setLanguage("el");
}

function setLanguage(lang) {
    if (!i18n[lang]) {
        return;
    }

    currentLanguage = lang;
    document.documentElement.lang = lang;
    document.title = i18n[lang].site_title;

    var textNodes = document.querySelectorAll("[data-i18n]");
    textNodes.forEach(function (node) {
        var key = node.getAttribute("data-i18n");
        if (i18n[lang][key]) {
            node.textContent = i18n[lang][key];
        }
    });

    var buttons = document.querySelectorAll(".lang-btn");
    buttons.forEach(function (button) {
        if (button.getAttribute("data-lang") === lang) {
            button.classList.add("is-active");
        } else {
            button.classList.remove("is-active");
        }
    });

    updateMapPopups();
}

function updateMapPopups() {
    if (!mapState.markers.length) {
        return;
    }

    mapState.markers.forEach(function (entry) {
        var stationName = i18n[currentLanguage][entry.station.nameKey];
        var stationText = i18n[currentLanguage][entry.station.textKey];
        entry.marker.bindPopup("<b>" + stationName + "</b><br>" + stationText);
    });
}
